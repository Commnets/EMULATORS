// ---
bool COMMODORE::MPS801BasicMatrixPrinterEmulation::printCharImplementation (unsigned char chr)
{
	// The default char to print out...
	char chrP = '?';
	// Not to print out anything by default... 
	// It will depend on whether a printable character was actually sent!
	bool result = false;

	// Line feed move the paper one line, 
	// but maintains the head of the printer in the same column...
	if (chr == 0x0a) 
	{ 
		moveHeadFromY (1); 
		
		printerFile () 
			<< std::endl 
			<< MCHEmul::_SPACES.substr (0, (size_t) headXPosition ());

		// Notice that "no office printing" is considered!
	}
	else
	// The carry return just move the head of the printer to the beginning of the line...
	if (chr == 0x0d)
	{
		unsigned short pX = headXPosition ();

		// This line can return the number of lines moved forward or backward...
		// ...but in this case that value is not taken into account (it will be for sure 0)...
		moveHeadFromX (-pX);

		printerFile () << std::string ("\b", configuration ()._charsPerLine).substr (pX);

		// Notice that "no office printing" is considered!
	}
	else
	// The TAB char...
	if (chr == 0x09)
	{
		if (moveHeadFromX (configuration ()._numSpacesTabChar) != 0)
			printerFile () 
				<< std::endl 
				<< MCHEmul::_SPACES.substr (0, (size_t) headXPosition ());

		// Notice that "no office printing" is considered!
	}
	else
	if (chr >= 0x20 && chr <= 0x5d) 
	{
		chrP = static_cast <char> (chr);

		result = true;
	}
	else
		result = true;

	if (result)
		_printerFile << chrP; // Just a char!...
	
	return (result);
}

// ---
void COMMODORE::MPS801PostscriptMatrixPrinterEmulation::firstTimePrinting (unsigned char chr)
{
	// Then the information about the routines to print out...
	// Part 1!
	printerFile () 
		<< std::ifstream ("PSMatrixPrinterI.ps").rdbuf () 
		<< std::endl;

	// Print out the information about the chars...
	for (const auto& i : configuration ()._charSet)
	{
		printerFile () << i.first << " [";

		bool fC = true;
		for (const auto& j : i.second)
		{
			printerFile () 
				<< (!fC ? " " : "") 
				<< "16#" << j.asString (MCHEmul::UByte::OutputFormat::_HEXA,2);
			fC = false;
		}

		printerFile () << "] putGlyph" << std::endl;
	}

	// Then the information about the routines to print out...
	// Part 2!..
	printerFile () 
		<< std::ifstream ("PSMatrixPrinterII.ps").rdbuf () 
		<< std::endl;

	// Print out information about the margins...
	// Just top start to print out at the right position in the page whatever its size was...
	printerFile ()
		<< std::to_string ((unsigned int) (paper ()._border /** in inches. */ * 72.0f /** points per inch. */)) + " " +
		   "0 " +
		   "topLeftWithMargin /y0 exch def /x0 exch def" << std::endl << std::endl;		

	_posXInside = _posYInside = 0;
}

// ---
bool COMMODORE::MPS801PostscriptMatrixPrinterEmulation::printCharImplementation (unsigned char chr)
{
	// The default char to print out...
	char chrP = '?';
	// Not to print out anything by default... 
	// It will depend on whether a printable character was actually sent!
	bool result = false;
	
	// Routine to print aout a byte of information and advance...
	auto printByteColumnAndAdvance = [&](const MCHEmul::UByte& byte) -> void
		{
			printerFile ()
				<< std::to_string ((unsigned int) byte.value ()) + "\t" +
				   "x0 " +
				   std::to_string ((unsigned int) _posXInside) + " " +
				   std::to_string ((unsigned int) _posX) + " " +
				   std::to_string ((unsigned int) (configuration ()._wChar)) + 
				   " mul add dotStepX mul add\ty0 " + // dotStepX defined in PSMatrixPrinterII.ps
				   std::to_string ((unsigned int) _posY) + " " +
				   "dotStepY " + // dotStepY & lineGapY defined in PSMatrixPrinterII.ps
				   std::to_string ((unsigned int) (configuration ()._hChar)) + 
				   " mul lineGapY add mul sub drawByteBits" << std::endl;

			// The definition of the dotStep...
			if ((++_posXInside == 6)) // No more than 6...
			{
				_posXInside = 0;

				moveHeadFromX (1);
			}
		};

	// Depending on the char received...
	switch (chr)
	{
		// Enter the graphic mode...
		case 0x08:
			{
				_graphical = true;
			}

			break;

		// Line feed (or cursor down) move the paper one line, 
		// but maintains the head of the printer in the same column...
		case 0x0a:
		case 0x11:
			{
				moveHeadFromY (1);
			}

			break;

		// Carry return or HOME
		case 0x0d:
		case 0x13:
			{
				moveHeadFromX (- (short) headXPosition ());
			}

			break;

		// Activate the double mode...
		case 0x0e:
			{
				_double = true;
			}

			break;

		// Activate the business mode (no double, no graphical)...
		case 0x0f:
			{
				_double = _graphical = false;
			}

			break;

		// Activating the tab setting...
		case 0x10:
			{
				_settingTab = true;
			}

			break;

		// Activate the reverse mode...
		case 0x12:
			{
				_reverse = true;
			}

			break;

		// Cursor up...
		case 0x91:
			{
				moveHeadFromY (-1);
			}

			break;

		case 0x92:
			{
				_reverse = false;
			}

			break;

		// In any other circunstance, 
		// what to do will depend on whether the graphical mode is or not aactive
		default:
			{
				if (_graphical)
				{
					printByteColumnAndAdvance (chr);
				}
				else
				{
					MCHEmul::MatrixPrinterEmulation::Configuration::CharSetDefinition::const_iterator i
						= _CONFIGURATION._charSet.find ((unsigned int) chr);
					if (i != _CONFIGURATION._charSet.end ())
					{
						printerFile () 
							<< "% Character " 
							<< std::to_string ((unsigned int) chr) << std::endl;
						for (const auto& j : (*i).second)
						{
							MCHEmul::UByte v = j; if (_reverse) v = ~v;
							printByteColumnAndAdvance (v);
							if (_double) printByteColumnAndAdvance (v); // twice...
						}
					}
					else
						moveHeadFromX (1);
				}
			}
	}
	
	return (result);
}

