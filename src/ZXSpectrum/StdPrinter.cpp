// ---
bool ZXSPECTRUM::ThermalPrinterSimulation::executeCommand (int cId, const MCHEmul::Strings& prms)
{
	bool result = false;

	switch (cId)
	{
		case 1: // Flush the printer...
			{
				result = true;
			}
			break;

		case 2: // Cut the page...
			{
				_emulation -> closeCurrentPage ();

				result = true;
			}
			break;

		default:
			// It shouldn't be here...
			break;
	}

	return (result);
}

// ---
bool ZXSPECTRUM::ThermalPrinterSimulation::simulate (MCHEmul::CPU* cpu)
{
	// What to do will depend on the simulation is...
	// Te definition can be in many different traps
	// Detect first whether the simulation is or not in one of them and then execute the right thing...
	// There can not be more than 1 trap with the same name
	bool nF = false;
	for (MCHEmul::Traps::const_iterator i = _traps.begin ();
		i != _traps.end () && !nF; i++)
	{
		if (nF = (cpu -> programCounter ().internalRepresentation () == (*i)._addressIn.value () && 
			(*i).verifyFingerTipAgainst (cpu -> memoryRef ()))) // The fingertip has to be also "found"...
		{
			// Empty any pending set action...
			// Because a trap is breaking the normal flow of the code...
			// so no set commands pendings can be left!
			MCHEmul::Memory::configuration ().executeMemorySetCommandsBuffered ();

			// Execute the trap...
			// ...and if it was executed, change the program counter
			// with the answer of the trap itself...
			// That can be the defined one or another...
			MCHEmul::Address rA;
			bool executed = executeTrap ((*i), cpu, rA);
			if (executed)
				cpu -> programCounter ().setAddress (rA);
		}
	}

	return (true);
}

// ---
MCHEmul::InfoStructure ZXSPECTRUM::ThermalPrinterSimulation::getInfoStructure () const
{
	MCHEmul::InfoStructure result = 
		std::move (MCHEmul::IOPeripheral::getInfoStructure ());

	result.add ("MatrixPrinterEmulation", std::move (_emulation -> getInfoStructure ()));
	result.add ("COMMANDS", MCHEmul::concatenateStrings (commandDescriptions (),"\n"));

	MCHEmul::InfoStructure trps;
	for (size_t i = 0; i < _traps.size (); i++)
		trps.add (std::to_string (i), _traps [i].asString ());
	result.add ("TRAPS", std::move (trps));

	return (result);
}

// ---
bool ZXSPECTRUM::ThermalPrinterSimulation::executeTrap 
	(const MCHEmul::Trap& t, MCHEmul::CPU* cpu, MCHEmul::Address& rA)
{
	// The CPU has to be from the F6500 family!
	assert (dynamic_cast <FZ80::CZ80*> (cpu) != nullptr);

	_IFDEBUG debugStatus (t.asString (), cpu);

	/** Whether the trap was or not executed. 
		Initially it is not. */
	bool result = false;
	/** The result when executed. */
	unsigned char st = 0;
	switch (t._id)
	{
		case _PRINT_A:
			{
				result = executePrintATrap (cpu, st, rA);
			}

			break;

		case _COPY_BUFF:
			{
				result = executeCopyBuffTrap (cpu, st, rA);
			}

			break;

		default:
			{
				_LOG ("Trap type not supported yet:" + 
					std::to_string (t._id));
				assert (false); // Just for compiling with debug on...
			}

			break;
	}

	return (result);
}

// ---
bool ZXSPECTRUM::ThermalPrinterSimulation::executePrintATrap 
	(MCHEmul::CPU* cpu, unsigned char& st, MCHEmul::Address& rA)
{
	rA = _traps [0]._addressOut;

	if (cpu -> memoryRef () -> value 
		(MCHEmul::Address (cpu -> memoryRef () -> values (_CURCHL, 2), false) + 0x04) == 0x50)
	{
		if (_charsBufferPointer == 32)
			_charsBufferPointer = 0;

		CharBufferElement val { 0x00 }; // Initially...
		val._char	= static_cast <FZ80::CZ80*> (cpu) -> aRegister ().values () [0].value ();
		val._status	= cpu -> memoryRef () -> value (_PFLAG).bit (2) ? 0x01 : 0x00;
		_charsBuffer [_charsBufferPointer++] = std::move (val);
	}

	return (true);
}

// ---
bool ZXSPECTRUM::ThermalPrinterSimulation::executeCopyBuffTrap
	(MCHEmul::CPU* cpu, unsigned char& st, MCHEmul::Address& rA)
{
	rA = MCHEmul::Address (cpu -> memoryRef () -> stack () -> pull (2), false);

	// Get the value of the register IY as an address...
	MCHEmul::Address iYA = static_cast <FZ80::CZ80*> (cpu) -> addressFromRegisters
		(static_cast <FZ80::CZ80*> (cpu) -> iyRegister ());

	// The printer prints the content of the buffer...
	// But that content is already formatted by the firmware at this point
	// So there is 256 bytes (8 lines of 32 bytes each)...
	// To use the routines of the postscript, the characters are turned into a virtual char
	// That is no used in any SINCLAIR machine and then sent to the printer...

	bool f = false;
	for (size_t i = 0; i < 32 && !f; i++)
	{
		// Has the char to be printed out in inverse?
		bool rvs = _charsBuffer [i]._status == 0x01;

		// The chars can have many different aspects...
		if (_charsBuffer [i]._char < 0x20)
		{
			// A blank char is printed instead of the control chars...
			_emulation -> printChar (0x20); 
			// Just to finish the printing...
			f = (_charsBuffer [i]._char == 0x13); 
		}
		else
		// UDG (User defined chras)
		if (_charsBuffer [i]._char >= 0x90 &&
			_charsBuffer [i]._char <= 0xa4)
		{
			// TODO
		}
		else
		// Normal char...
		if (_charsBuffer [i]._char >= 0x20 &&
			_charsBuffer [i]._char <= 0x8f)
		{
			// Printing the normal char taking into account
			// whether the reverse video is or not active...
			_emulation -> printChar ((rvs ? 0x80 : 0x00) + _charsBuffer [i]._char);
		}
		else
		// Token...
		if (_charsBuffer [i]._char >= 0xa5)
		{
			// TODO
		}
	}

	// Clears the internal buffer...
	for (size_t i = 0; i < 32; _charsBuffer [i++] = { 0x00 });
	_charsBufferPointer = 0;

	// Clears the computer buffer...
	for (size_t i = 0; i < _BLEN; 
		cpu -> memoryRef () -> set (_PRBUFF + i++, MCHEmul::UByte::_0));
	// Set the counter of positions to 0
	cpu -> memoryRef () -> set (iYA + _PR_CC_POS, MCHEmul::UByte::_0);
	// FLAG 2 bit 1 to indicate that the buffer is empty...
	cpu -> memoryRef () -> set (iYA + _PR_FLAG2_POS, 
		cpu -> memoryRef () -> value (iYA + _PR_FLAG2_POS) & ~0x02);

	// The register A and B are set also to 0
	static_cast <FZ80::CZ80*> (cpu) -> aRegister ().set ({ 0x00 });
	static_cast <FZ80::CZ80*> (cpu) -> bRegister ().set ({ 0x00 });
	// The register C must be set to 0x21...
	static_cast <FZ80::CZ80*> (cpu) -> cRegister ().set ({ 0x21 });

	return (true);
}

// ---
void ZXSPECTRUM::ThermalPrinterSimulation::debugStatus 
	(const std::string& where, MCHEmul::CPU* cpu)
{
	auto allRgsAsAttrs = [&](const MCHEmul::Registers& rgs) -> MCHEmul::Attributes
		{ MCHEmul::Attributes result;
		  for (const auto& i : rgs) 
			  result.insert (MCHEmul::Attributes::value_type (i.name (), i.asString ()));
		  return (result); };

	_deepDebugFile -> writeSimpleLine (where);
	_deepDebugFile -> writeCompleteLine ("SerialIO", cpu -> clockCycles (), 
		"Registers:", allRgsAsAttrs (cpu -> internalRegisters ()));
}
